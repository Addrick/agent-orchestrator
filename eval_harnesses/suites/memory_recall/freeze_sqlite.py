"""Export a slice of user_memory.db as SQL for fixture replay.

Given a list of segment_ids, dumps INSERT statements for:
    - Memory_Segments rows
    - Memory_Summaries rows (all levels) for those segments, including
      the embedding BLOB so recall works without re-embedding
    - User_Interactions rows in each segment's start..end_interaction_id
      range (for summary refinement / grader inspection)
    - Message_Embeddings rows for those interactions (so KNN over raw
      messages also works on the slice)

Schema is NOT dumped. The loader calls MemoryManager.create_schema() on a
fresh DB and then execs the slice SQL — migration-safe.

Usage:
    python -m eval_harnesses.suites.memory_recall.freeze_sqlite \\
        --segments 743,663,620,573,574 \\
        --out eval_harnesses/suites/memory_recall/fixtures/slice.sql \\
        [--db data/user_memory.db]
"""
from __future__ import annotations

import argparse
import sqlite3
import sys
from pathlib import Path
from typing import Iterable, List, Sequence

DEFAULT_DB = "data/user_memory.db"

_SEGMENT_COLS = [
    "segment_id", "channel", "server_id", "persona_name",
    "start_interaction_id", "end_interaction_id", "message_count",
    "created_at", "first_message_at", "last_message_at",
]
_SUMMARY_COLS = [
    "summary_id", "segment_id", "content", "embedding", "model_name",
    "created_at", "summary_level", "parent_summary_id", "untrusted",
]
_INTERACTION_COLS = [
    "interaction_id", "user_identifier", "persona_name", "channel",
    "author_role", "author_name", "content", "timestamp",
    "zammad_ticket_id", "platform_message_id", "server_id",
    "tool_context", "parent_summary_id", "reply_to_id", "reasoning_content",
]
_MESSAGE_EMBED_COLS = [
    "interaction_id", "embedding", "model_name", "created_at",
]


def _lit(v) -> str:
    if v is None:
        return "NULL"
    if isinstance(v, (bytes, bytearray, memoryview)):
        return "X'" + bytes(v).hex() + "'"
    if isinstance(v, (int, float)):
        return repr(v)
    s = str(v).replace("'", "''")
    return f"'{s}'"


def _row_values(row: sqlite3.Row, cols: Sequence[str]) -> str:
    return "(" + ", ".join(_lit(row[c]) for c in cols) + ")"


def _emit_inserts(
    out, table: str, cols: Sequence[str], rows: Iterable[sqlite3.Row]
) -> int:
    rows = list(rows)
    if not rows:
        out.write(f"-- {table}: 0 rows\n\n")
        return 0
    out.write(f"-- {table}: {len(rows)} rows\n")
    col_list = ", ".join(cols)
    for r in rows:
        out.write(f"INSERT INTO {table} ({col_list}) VALUES {_row_values(r, cols)};\n")
    out.write("\n")
    return len(rows)


def freeze(db_path: str, segment_ids: Sequence[int], out_path: Path) -> None:
    con = sqlite3.connect(db_path)
    con.row_factory = sqlite3.Row

    placeholders = ",".join("?" for _ in segment_ids)
    segs = con.execute(
        f"SELECT {', '.join(_SEGMENT_COLS)} FROM Memory_Segments "
        f"WHERE segment_id IN ({placeholders}) ORDER BY segment_id",
        list(segment_ids),
    ).fetchall()
    if len(segs) != len(segment_ids):
        found = {s["segment_id"] for s in segs}
        missing = [s for s in segment_ids if s not in found]
        print(f"WARNING: segments not found in source DB: {missing}", file=sys.stderr)

    sums = con.execute(
        f"SELECT {', '.join(_SUMMARY_COLS)} FROM Memory_Summaries "
        f"WHERE segment_id IN ({placeholders}) "
        f"ORDER BY segment_id, summary_level, summary_id",
        list(segment_ids),
    ).fetchall()

    # Collect interaction_id ranges per segment, union them.
    ranges = [(s["start_interaction_id"], s["end_interaction_id"]) for s in segs]
    if ranges:
        union_clause = " OR ".join(
            "(interaction_id BETWEEN ? AND ?)" for _ in ranges
        )
        params: List[int] = []
        for a, b in ranges:
            params.extend([a, b])
        ints = con.execute(
            f"SELECT {', '.join(_INTERACTION_COLS)} FROM User_Interactions "
            f"WHERE {union_clause} ORDER BY interaction_id",
            params,
        ).fetchall()
        int_ids = [r["interaction_id"] for r in ints]
        if int_ids:
            iph = ",".join("?" for _ in int_ids)
            msg_embeds = con.execute(
                f"SELECT {', '.join(_MESSAGE_EMBED_COLS)} FROM Message_Embeddings "
                f"WHERE interaction_id IN ({iph}) ORDER BY interaction_id",
                int_ids,
            ).fetchall()
        else:
            msg_embeds = []
    else:
        ints = []
        msg_embeds = []

    out_path.parent.mkdir(parents=True, exist_ok=True)
    with out_path.open("w", encoding="utf-8") as out:
        out.write(
            "-- memory_recall slice. Generated by freeze_sqlite.py.\n"
            f"-- Source DB: {db_path}\n"
            f"-- Segments: {sorted(segment_ids)}\n"
            "-- Loader: create_schema() then exec this file. The schema's\n"
            "-- post-create sync copies Memory_Summaries.embedding into\n"
            "-- vec_Memory_Summaries automatically.\n\n"
            "BEGIN;\n\n"
        )
        n_seg = _emit_inserts(out, "Memory_Segments", _SEGMENT_COLS, segs)
        n_int = _emit_inserts(out, "User_Interactions", _INTERACTION_COLS, ints)
        n_msg = _emit_inserts(out, "Message_Embeddings", _MESSAGE_EMBED_COLS, msg_embeds)
        n_sum = _emit_inserts(out, "Memory_Summaries", _SUMMARY_COLS, sums)
        out.write("COMMIT;\n")

    print(
        f"Wrote {out_path}\n"
        f"  segments:     {n_seg}\n"
        f"  interactions: {n_int}\n"
        f"  msg_embeds:   {n_msg}\n"
        f"  summaries:    {n_sum}",
        file=sys.stderr,
    )
    con.close()


def main(argv=None) -> int:
    p = argparse.ArgumentParser(prog="freeze_sqlite", description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--db", default=DEFAULT_DB)
    p.add_argument("--segments", required=True,
                   help="comma-separated segment_ids to freeze")
    p.add_argument("--out", required=True, type=Path,
                   help="output .sql path")
    args = p.parse_args(argv)
    seg_ids = [int(x) for x in args.segments.split(",") if x.strip()]
    if not seg_ids:
        print("ERROR: --segments empty", file=sys.stderr)
        return 2
    freeze(args.db, seg_ids, args.out)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
